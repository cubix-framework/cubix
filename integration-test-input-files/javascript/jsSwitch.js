function main(){
    var i = 9;  
    switch (i)   
    {  
       case 0:  
           console.log("i is zero.");  
           break;  
       case 1:  
           console.log("i is one.");  
       case 2:  
           console.log("i is two.");  
           break;  
       default:  
           console.log("i is greater than 2.");  
    }
}
