class IfElseDemo { 
    public static void main(String args[]) 
    { 
        int i = 20; 
  
        if (i < 15) 
            System.out.println("i is smaller than 15"); 
        else
            System.out.println("i is greater than 15"); 
  
        System.out.println("Outside if-else block"); 
    } 
}
